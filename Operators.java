public class Operators {
    public static void main(String args[]){
        //Arithmetic Operator
        int a=4;
        //int b=6%a;//Modulo Operator
        int b=9;
        //Assignment Operator
         b *=3;
        System.out.println(b);
        //Comparison Operator
        System.out.println(6==8);
        System.out.println(64>6);
        System.out.println(64<6);

        //Logical Operators
        System.out.println(64<6 && 6==8);
        System.out.println(64<6 || 6==8);

        //Bitwise Operators
        System.out.println(2&3);


    }
}
